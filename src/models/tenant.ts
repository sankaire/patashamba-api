import { Schema, model } from "mongoose";
import { ITenant } from "../interface/index.js";

const tenantSchema = new Schema<ITenant>(
  {
    userName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    country: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Tenant = model("Tenant", tenantSchema);
export default Tenant;
